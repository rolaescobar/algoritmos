# Algoritmo para calcular el volumen de una caja

# Leer las medidas A, B y C
A = float(input("Ingrese la medida A: ")) 
B = float(input("Ingrese la medida B: "))
C = float(input("Ingrese la medida C: "))

# Calcular el volumen
volumen = A * B * C


# Mostrar el resultado
print("El volumen de la caja es:", volumen,"cm")

